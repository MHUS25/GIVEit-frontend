class PersistentDrawerLeft extends React.Component {
  state = {
    open: false,
  };

  handleDrawerOpen = () => {
    this.setState({ open: true });
  };

  handleDrawerClose = () => {
    this.setState({ open: false });
  };
  handleSubmit = listing => {
    this.setState({ listings: [...this.state.listings, listing] });
    fetch('https://giveit-backend.herokuapp.com/listings', {
      method: 'POST',
      headers: {
        Accept: 'application/json',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        listing
      }),
    }).then((response) => response.json())
      .then((responseJson) => {
        return responseJson.listing;
      })
  };
  render() {
    const { classes, theme } = this.props;
    const { open } = this.state;

    return (
      <div className={classes.root}>
        <CssBaseline />
        <AppBar
          position="fixed"
          className={classNames(classes.appBar, {
            [classes.appBarShift]: open,
          })}
        >
          <Toolbar disableGutters={!open}>
            <IconButton
              color="inherit"
              aria-label="Open drawer"
              onClick={this.handleDrawerOpen}
              className={classNames(classes.menuButton, open && classes.hide)}
            >
              <MenuIcon />
            </IconButton>
            <Typography variant="h6" color="inherit" noWrap>
              GIVEit
            </Typography>
            <div className={classes.grow} />
            <div className={classes.search}>
              <div className={classes.searchIcon}>
                <SearchIcon />
              </div>

              <InputBase
                placeholder="Searchâ€¦"
                classes={{
                  root: classes.inputRoot,
                  input: classes.inputInput,
                }}
              />
            </div>
            <IconButton className={classes.menuButton} color="inherit" aria-label="Open drawer">
              <Button variant="contained" color="primary" className={classes.button}>
                Sign Up
                {/* This Button uses a Font Icon, see the installation instructions in the docs. */}
              </Button>
              <Button variant="contained" color="primary" className={classes.button}>
                Log In
                {/* This Button uses a Font Icon, see the installation instructions in the docs. */}
              </Button>
            </IconButton>
          </Toolbar>

        </AppBar>

        <Drawer
          className={classes.drawer}
          variant="persistent"
          anchor="left"
          open={open}
          classes={{
            paper: classes.drawerPaper,
          }}
        >
          <div className={classes.drawerHeader}>
            <IconButton onClick={this.handleDrawerClose}>
              {theme.direction === 'ltr' ? <ChevronLeftIcon /> : <ChevronRightIcon />}
            </IconButton>
          </div>
          <Divider />
          <FormListings handleSubmit={this.handleSubmit} />
          <Divider />

        </Drawer>
        <main
          className={classNames(classes.content, {
            [classes.contentShift]: open,
          })}
        >
          <div className={classes.drawerHeader} />

          <Typography paragraph>
            Welcome to GIVEit
            Join our GIVEit community and give what can.

            Post it if you are in need of help our GIVEit Map is here to help!
          </Typography>
          <Typography paragraph>
            Anything else you want to add...
          </Typography>

            <div id="map" />

        </main>
      </div>
    );
  }
}

